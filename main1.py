def traiter_choix(choix):
    if choix == "1":
        print("Vous avez choisi : Café ☕")
    elif choix == "2":
        print("Vous avez choisi : Thé 🍵")
    elif choix == "3":
        print("Vous avez choisi : Eau chaude 💧")
    elif choix == "4":
        print("Merci et à bientôt ! 👋")
    else:
        print("Choix invalide. Veuillez entrer un numéro entre 1 et 4.")